<div class=''>
    <div class='text-base'>Adding Product Specifications</div>
    <div class="text-sm">When detailing product specifications such as the brand, weight, dimensions, and other relevant
        characteristics, please ensure to include comprehensive and precise information. This enhances the product's
        description and assists customers in making informed decisions.
    </div>
    <div class='font-bold text-base mt-2'>Important Guidelines:</div>
    <ul class='text-base mt-2' style="list-style-type: disc">
        <li class="mt-1">
            <div>For attributes that offer multiple choices (e.g., color, size, gender), denote alternatives using a
                slash
                <span class='font-black'>"/"</span>
                to separate each option. This notation aids in clarity and simplifies the selection process for users.
            </div>
            <div><span class="font-bold">Example</span>: For a gender option, input "Male / Female" to
                indicate the available choices.
            </div>
        </li>
        <li class="mt-1">
            <div>
                To cater to a diverse audience, include translations for key attributes to make your product accessible
                to non-English speakers.
            </div>
            <div>
                <span class="font-bold">For English translations</span>, append "_en" to the attribute name and provide
                the options in English.
                <span class="font-semibold">Example:</span> Use "gender_en" for gender, with the options "Male /
                Female".
            </div>
            <div>
                <span class="font-bold">For Arabic translations</span>, append "_ar" to the attribute name and provide
                the options in Arabic.
                <span class="font-semibold">Example:</span> Use "gender_ar" for gender, with the options "ذكر / أنثى".
            </div>
        </li>
    </ul>
</div>
